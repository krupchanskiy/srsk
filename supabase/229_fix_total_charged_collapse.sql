-- Баг из ночного тестирования: первое же начисление в финмодуле обнуляло сумму
-- услуг CRM. Воспроизведено: услуга 45 000 + начисление 500 → total_charged = 500,
-- гость в портале увидел бы переплату 44 500.
--
-- Причина: бинарный переключатель «есть фин-начисления → показываем только их».
-- Решение: складываем. Это два независимых источника начислений, а не два
-- представления одного: услуги CRM (crm_deal_services) и начисления финмодуля
-- заводятся разными людьми в разных местах. Сегодня услуг в базе нет вовсе (0 строк),
-- так что живого эффекта смена не даёт — снимается будущая ловушка.
--
-- Направление отказа выбрано намеренно: при ошибочном задвоении гость увидит
-- завышенный долг и пожалуется (самокорректируется), при обрыве — ложную
-- переплату и решит, что ничего не должен (потеря денег).
--
-- ВАЖНО для cutover: переносить историю в fin_participant_opening_balances,
-- НЕ дублируя её услугами CRM — иначе суммы сложатся дважды.
--
-- Полный текст функции — в применённой миграции на проде
-- (SELECT pg_get_functiondef('crm_calc_deal_totals'::regproc)); отличие от 217 —
-- последняя строка: o_charged := round(v_services + v_fin_charged, 2)
-- вместо CASE WHEN v_has_fin_charged THEN v_fin_charged ELSE v_services END.

REVOKE ALL ON FUNCTION public.crm_calc_deal_totals(uuid) FROM PUBLIC, anon, authenticated;
