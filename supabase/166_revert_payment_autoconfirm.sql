-- =============================================================================
-- Миграция 166: Откат автоподтверждения платежей из миграции 163
-- =============================================================================
-- Решение: существующие платежи должны пройти ревизию Нитьей-виласини,
-- а не считаться подтверждёнными автоматически.
--
-- Откатываем только те, где confirmed_by IS NULL — т.е. те, что пометил
-- бэкфилл миграции 163. Если кто-то уже подтверждал вручную через UI после
-- выката PR 2 (`confirmed_by` заполнен) — такие не трогаем.
--
-- Сами записи crm_payments НЕ удаляются и НЕ изменяются — только флаг
-- подтверждения возвращается к дефолтному FALSE. Триггер update_deal_total_paid
-- пересчитает crm_deals.total_paid (временно 0 по всем сделкам, пока
-- Нитья не подтвердит каждый платёж в очереди).
-- =============================================================================

UPDATE crm_payments
   SET is_confirmed = FALSE,
       confirmed_at = NULL,
       confirmed_by = NULL
 WHERE is_confirmed = TRUE
   AND confirmed_by IS NULL;

-- Явный пересчёт total_paid (триггер и так сработал построчно, но для гарантии)
UPDATE crm_deals d SET total_paid = COALESCE(
    (SELECT SUM(amount_inr) FROM crm_payments WHERE deal_id = d.id AND is_confirmed = TRUE),
    0);
